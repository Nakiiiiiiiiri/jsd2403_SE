package string;
//to Upper/Lower case将数组中的英文转为大写/小写
public class toUpperCase {
    public static void main(String[] args) {
        String str = "我爱java";
        String upper = str.toUpperCase();
        System.out.println(upper);
        String lower = str.toLowerCase();
        System.out.println(lower);

    }
}
